    public Persona recuperarDatosGUI(){
        Persona persona = new Persona();
        
        if (txtId.getText().length() > 0){
            persona.setId(Integer.parseInt(txtId.getText()));
        }
        else{
            persona.setId(0);
        }
        if (txtRut.getText().length() > 0){
            persona.setRut(txtRut.getText()+"-"+cboxDigito.getSelectedItem());
        }
        else{
            persona.setRut(null);
        }
        if (txtNombre.getText().length() > 0){
            persona.setNombre(txtNombre.getText());   
        }
        else{
            persona.setNombre(null);
        }
        if (txtAppaterno.getText().length() > 0){
            persona.setApellido_paterno(txtAppaterno.getText());
        }
        else{
            persona.setApellido_paterno(null);
        }
        if (txtApmaterno.getText().length() > 0){
            persona.setApellido_materno(txtApmaterno.getText());
        }
        else{
            persona.setApellido_materno(null);
        }
        if (txtEmail.getText().length() > 0){
            persona.setEmail(txtEmail.getText());
        }
        else{
            persona.setEmail(null);
        }
        if (cboxComuna.getSelectedIndex() > 0){
            persona.setId_direccion(getDireccionId(txtDireccion.getText(), txtDireccionNumero.getText(), Integer.parseInt(String.valueOf(cboxComuna.getSelectedItem().toString().charAt(0)))));
            persona.setId_comuna(Integer.parseInt(String.valueOf(cboxComuna.getSelectedItem().toString().charAt(0))));
        }else{
            persona.setId_direccion(0);
            persona.setId_comuna(0);
        }
        if (cboxRegion.getSelectedIndex() > 0){
            persona.setId_region(Integer.parseInt(String.valueOf(cboxRegion.getSelectedItem().toString().charAt(0))));
        }else{
            persona.setId_region(0);
        }
        
        if (cboxCiudad.getSelectedIndex() > 0){
            persona.setId_ciudad(Integer.parseInt(String.valueOf(cboxCiudad.getSelectedItem().toString().charAt(0))));
        }else{
            persona.setId_ciudad(0);
        }
        
        return persona;
    }
    
    public int getDireccionId(String calle, String numero, int id_comuna){
        Controlador conn = new Controlador();
        
        String sentencia = String.format("call PD_INSERT_DIRECCION('%s', '%s', %s)", calle, numero, id_comuna);
        //System.out.println("Sentencia: "+sentencia);
        conn.ejecutarProcedimientoAlmacenado(sentencia);
        //JOptionPane.showMessageDialog(this, "Dirección añadida correctamente!");
        int last_id = conn.getIdDireccion();
        
        return last_id;
    }
    

    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        Controlador conn = new Controlador();
        
        Rol rol = recuperarDatosGUI();
        
        try {
            if (rol.getNombre().length() == 0){
                JOptionPane.showMessageDialog(this, "Debe llenar todos los campos requeridos");
            }
            else if (rol.getNombre().length() >= 3){
                String sentencia = String.format("call PD_INSERT_ROL('%s')", rol.getNombre());
                System.out.println("Sentencia: "+sentencia);
                conn.ejecutarProcedimientoAlmacenado(sentencia);
                JOptionPane.showMessageDialog(this, "Rol creado correctamente!");
            }
            else{
                JOptionPane.showMessageDialog(this, "Rol creado debe tener al menos 3 caracteres...");
            }
        } 
        catch (Exception e) {
            System.out.println("Error: "+e);
        }
        finally{
            model.setRowCount(0);
            mostrarDatos();
            conn.desconectar();
            limpiarCampos();
        }
    }//GEN-LAST:event_btnCrearActionPerformed


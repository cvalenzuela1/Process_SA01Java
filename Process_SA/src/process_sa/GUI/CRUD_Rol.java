    public Rol recuperarDatosGUI(){
        Rol rol = new Rol();
        
        if (txtId.getText().length() > 0){
            rol.setId(txtId.getText());
        }
        else{
            rol.setId(null);
        }
        rol.setNombre(txtRol.getText().substring(0,1).toUpperCase() + txtRol.getText().substring(1));
        
        return rol;
    }
    
    public void limpiarCampos(){
        txtId.setText(null);
        txtRol.setText(null);
    }
    
    public void mostrarDatos(){
        Controlador conn = new Controlador();
        
        try {
            final List<String> listaUsuario = conn.getRolesDistinct();
            for (String listaUsuarios : listaUsuario) {
                Object[] o = {listaUsuarios};
                model.addRow(o);
            }
        } catch (Exception e) {
            System.out.println("Error: "+e);
        }
        conn.desconectar();
    }
    
